void main(){
  double panjang;
  double lebar;
  double luas;
  double keliling;

  panjang = 10;
  lebar = 4;

  luas = panjang * lebar;
  keliling = 2 * (panjang + lebar);

  print('Luas persegi panjang: ' + luas.toString());
  print('Keliling persegi panjang: ' + keliling.toString());
}